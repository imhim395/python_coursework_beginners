fruits = {"apple", "orange", "banana", "tomato"}
techstuff = {"mac", "code", "ai"}
combo = techstuff.union(fruits)#combines two sets together
print(combo)

fruits = {"apple", "orange", "banana", "tomato"}
techstuff = {"mac", "code", "ai"}
combo = techstuff | fruits#combines two sets together as well
print(combo)